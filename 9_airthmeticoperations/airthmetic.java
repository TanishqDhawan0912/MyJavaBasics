class Main {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;
        int c = a + b;
        System.out.println(c); // addition

        int d = a / b;
        System.out.println(d); // division

        int e = a * b;
        System.out.println(e); // multiplication

        int f = a - b;
        System.out.println(f); // subtraction

        int g = a % b;
        System.out.println(g); // modulus

        
    }
}