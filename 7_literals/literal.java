class literal {
    public static void main(String[] args) {
        int num = 0b101; // binary literal
        int num1 = 0x1A; // hexadecimal literal
        int num2 = 1_00_00_000; // underscore in number
        double num3 = 123; // double literal
        double num4 = 12e19; // scientific notation
        char ch = 'a'; // character literal
        ch++; // increment character
        System.out.println(num);
        System.out.println(num1);
        System.out.println(num2);
        System.out.println(num3);
        System.out.println(num4);
        System.out.println(ch);

    }
}